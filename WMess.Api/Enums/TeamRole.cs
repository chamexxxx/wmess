namespace WMess.Api.Enums;

/// <summary>
/// Роль пользователя в команде.
/// Значения упорядочены от наименьших прав к наибольшим, чтобы значение по умолчанию (0)
/// соответствовало наименее привилегированной роли (fail-safe).
/// </summary>
public enum TeamRole
{
    /// <summary>
    /// Участник - только работа с контентом (чаты, документы, календарь)
    /// </summary>
    Member = 0,

    /// <summary>
    /// Администратор - управление командой и проектами, без права удаления команды
    /// </summary>
    Admin = 1,

    /// <summary>
    /// Владелец команды - полный контроль, включая удаление команды и смену ролей
    /// </summary>
    Owner = 2
}
