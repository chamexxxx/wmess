namespace WMess.Api.Enums;

/// <summary>
/// Тип чата. Определяет, к какой сущности привязан чат и как вычисляются права.
/// </summary>
public enum ChatType
{
    /// <summary>Командный чат — доступ = участник команды, управление = Admin/Owner.</summary>
    Team = 0,

    /// <summary>Проектный чат — доступ = доступ к проекту, управление = управление проектом.</summary>
    Project = 1,

    /// <summary>Личный чат 1-на-1 (зарезервировано, не используется в текущей реализации).</summary>
    Direct = 2
}
